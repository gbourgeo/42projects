#include "libft.h"

char		*ft_strcharset(char *s1, char *s2)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (s1 && s2)
	{
		while (s1[i] != '\0')
		{
			while (s2[j] != '\0')
			{
				if (s1[i] == s2[j])
					return (s1 + i);
				j++;
			}
			j = 0;
			++i;
		}
	}
	return (NULL);
}
