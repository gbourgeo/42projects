#include "libft.h"
#include "cl_main.h"

void			cl_ncurses_copy(char *s)
{
	int			i;

	i = ft_strlen(s) + 1;
	s[i] = '\0';
	while (--i > 0)
		s[i] = s[i - 1];
}
