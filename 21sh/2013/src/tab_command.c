#include "main.h"

void				tab_command(t_env *e)
{
	if (e->pos == e->q_pos || e->hist->cmd[e->pos] == TAB ||
		e->hist->cmd[e->pos - 1] == TAB)
		check_cmd(TAB, e);
}
