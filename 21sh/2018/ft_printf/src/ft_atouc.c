#include "ft_printf.h"

unsigned char		ft_atouc(char *str)
{
	int				i;
	unsigned char	c;

	i = 0;
	c = 0;
	while (i < 8)
	{
		if (str[i] == '1')
			c += ft_pow(2, 7 - i);
		i++;
	}
	return (c);
}
