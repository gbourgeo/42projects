#include "main.h"

void		ft_clear_image(t_env *e)
{
	t_point		p;

	p.y = 0;
	while (p.y < e->img.h - 1)
	{
		p.x = 0;
		while (p.x < e->img.w - 1)
		{
			ft_put_color(e, &p, e->bckgrd);
			p.x++;
		}
		p.y++;
	}
}
