#include "main.h"

void		ps_swap(t_lib **a, char **ret, char *c)
{
	int		i;

	if (*a)
	{
		if ((*a)->next)
		{
			if ((*a)->nb > (*a)->next->nb)
			{
				i = (*a)->nb;
				(*a)->nb = (*a)->next->nb;
				(*a)->next->nb = i;
				*ret = ft_strjoin(*ret, c);
			}
		}
	}
}
